#!/usr/bin/env python3

""" demonstrates various ways of creating a set """

# using ()
setA = set()
print(len(setA), ":", setA)

# the duplicates will be removed
setB = {"mississippi"}
print(len(setB), ":", setB)

#using {}
setC = {"Sun", "Mon", "Tue", "Wed", "Thu", "Fri", "Sat"}
print(len(setC), ":", setC)

